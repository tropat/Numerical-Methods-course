def func(x0):
    x = [0.062500, 0.187500, 0.312500, 0.437500, 0.562500, 0.687500, 0.812500, 0.937500]
    y = [0.687959, 0.073443, -0.517558, -1.077264, -1.600455, -2.080815, -2.507266, -2.860307]
    n = len(x)
    sum = 0
    for j in range(0, n):
        r = 1
        for i in range(0,n):
            if j!=i:
                r *= (x0 - x[i])/(x[j]-x[i])
        sum += r*y[j]
    return sum

